/*
alice wins if she has all 1's

*/

void solve() {
    int n; cin >> n;
    vi a(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    if (a[0] == 0 && a[n - 1] == 0) cout << "Bob" << endl;
    else cout << "Alice" << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    
}
