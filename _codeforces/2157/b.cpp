void solve() {
    int n, x, y; cin >> n >> x >> y;
    int tot = abs(x) + abs(y);
    string s; cin >> s;
    for(char c : s) {
        if (c == '4') tot -= 1;
        else tot -= 2;
    }
    if (tot <= 0 && n >= abs(x) && n >= abs(y)) cout << "YES" << endl;
    else cout << "NO" << endl;
}

