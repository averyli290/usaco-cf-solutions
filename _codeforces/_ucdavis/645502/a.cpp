const long long INF = 1e18;


void solve() {
    int n, m; cin >> n >> m;
    vector<vi> a(n, vi(m, 0));
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < m; j++) {
            cin >> a[i][j];
        }
    }
    if (n == 1 && m == 1) {
        cout << -1 << endl;
    } else {
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < m; j++) {
                cout << (a[i][j]) % (n * m) + 1 << " ";
            }
            cout << endl;
        }
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    
}
