void solve() {
    int n; cin >> n;
    vll a(n), b(n);
    for(int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for(int i = 0; i < n; i++) {
        cin >> b[i];
    }
    if (b[n - 1] != a[n - 1]) {
        cout << "NO" << endl;
        return;
    }
    for(int i = n - 1; i >= 0; i--) {
        if (a[i] != b[i] && b[i] != (a[i + 1] ^ a[i]) && b[i] != (b[i + 1] ^ a[i])) {
            cout << "NO" << endl;
            return;
        }
    }
    cout << "YES" << endl;
}

